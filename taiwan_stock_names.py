def get_stock_name(symbol: str) -> str:
    """
    根據股票代碼獲取中文名稱
    
    參數:
        symbol: 股票代碼（不含後綴）
        
    返回:
        中文名稱，如果找不到則返回代碼本身
    """
    return TAIWAN_STOCK_NAMES.get(symbol, symbol)

def has_stock_name(symbol: str) -> bool:
    """
    檢查是否有該股票的中文名稱
    
    參數:
        symbol: 股票代碼
        
    返回:
        True 如果有名稱，否則 False
    """
    return symbol in TAIWAN_STOCK_NAMES
